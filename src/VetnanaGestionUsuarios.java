    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       String email = txtCorreo.getText().trim(); // Capturar el email del empleado desde el formulario

    if (email.isEmpty()) {
        mensaje("ERROR: Campo email vac√≠o");
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(
        this,
        "¬øSeguro que quieres eliminar al empleado?",
        "Confirmar eliminaci√≥n",
        JOptionPane.YES_NO_OPTION
    );

    if (confirmacion != JOptionPane.YES_OPTION) {
        mensaje("Operaci√≥n cancelada");
        return;
    }

    try {
        // Llamar al m√©todo para eliminar al empleado
        if (bd.eliminarEmpleado(email)) {
            mensaje("Empleado eliminado exitosamente");
            limpiarCampos(); // Limpiar los campos del formulario
        } else {
            mensaje("Error al eliminar el empleado");
        }
    } catch (Exception e) {
        mensaje("ERROR: Ocurri√≥ un problema al eliminar el empleado");
    }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

