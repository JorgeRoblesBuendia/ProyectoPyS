    public void actualizarTabla(){
        ArrayList<String[]>datos =bd.mostrarProductos();
        if(datos.size()==0)return;
        int totalRenglones=m.getRowCount();
        for (int i = 0; i <totalRenglones; i++) {//hh
            m.removeRow(0);
        }
        for (String[] data: datos) {
            m.addRow(data);
        }   
    }
